/* ============================================================
   LindbladTTN Input Generator
   Reads the form, emits a ready-to-run Python script.
   ============================================================ */

'use strict';

(() => {
  const form = document.getElementById('gen-form');
  const out  = document.getElementById('gen-output');
  const copy = document.getElementById('gen-copy');
  if (!form || !out) return;

  function val(name, fallback = '') {
    const el = form.elements.namedItem(name);
    return el ? el.value : fallback;
  }

  function checked(name) {
    const el = form.elements.namedItem(name);
    return el ? !!el.checked : false;
  }

  function indent(s, n = 4) {
    const pad = ' '.repeat(n);
    return s.split('\n').map(l => l ? pad + l : l).join('\n');
  }

  function emit() {
    const n_sites   = parseInt(val('n_sites', '2'), 10);
    const bond_dim  = parseInt(val('bond_dim', '16'), 10);
    const topology  = val('topology', 'tree');
    const strategy  = val('strategy', 'ps1');
    const device    = val('device', 'cpu');
    const dtype     = val('dtype', 'complex128');
    const t_final   = parseFloat(val('t_final', '10.0'));
    const dt        = parseFloat(val('dt', '0.01'));
    const gamma     = parseFloat(val('gamma', '0.1'));
    const drive     = checked('drive');
    const omega     = parseFloat(val('omega', '1.0'));
    const omega_r   = parseFloat(val('omega_r', '0.4'));

    let driveBlock = '';
    let driveArgs  = '';
    if (drive) {
      driveBlock = `
def drive(t):
    return ${omega_r} * np.cos(${omega} * t)
`;
      driveArgs = `
    f=drive,
    V=X_all[0],          # drive qubit 0 via sigma_x`;
    }

    const code =
`# Auto-generated by the LindbladTTN Input Generator.
# https://github.com/<your-org>/lindblad_ttn
import numpy as np
from lindblad_ttn import LindbladTTN

# ---- Pauli operators ------------------------------------------------
I2 = np.eye(2, dtype=complex)
X  = np.array([[0,  1 ], [1,  0]], dtype=complex)
Y  = np.array([[0, -1j], [1j, 0]], dtype=complex)
Z  = np.array([[1,  0 ], [0, -1]], dtype=complex)
sm = np.array([[0,  0 ], [1,  0]], dtype=complex)

n_sites = ${n_sites}

def kron_op(local_op, site, n):
    """Embed a single-qubit operator on \`site\` into the n-qubit space."""
    out = local_op if site == 0 else I2
    for i in range(1, n):
        out = np.kron(out, local_op if i == site else I2)
    return out

X_all  = [kron_op(X,  s, n_sites) for s in range(n_sites)]
Z_all  = [kron_op(Z,  s, n_sites) for s in range(n_sites)]
sm_all = [kron_op(sm, s, n_sites) for s in range(n_sites)]

# ---- Hamiltonian ----------------------------------------------------
omega = ${omega}
H0 = 0.5 * omega * sum(Z_all)
${driveBlock}
# ---- Lindblad jump operators ----------------------------------------
gamma = ${gamma}
L_ops = [(gamma, sm_all[s]) for s in range(n_sites)]

# ---- Solver ---------------------------------------------------------
solver = LindbladTTN(
    H0=H0,${driveArgs}
    L_ops=L_ops,
    n_sites=n_sites,
    bond_dim=${bond_dim},
    topology=${JSON.stringify(topology)},
    strategy=${JSON.stringify(strategy)},
    device=${JSON.stringify(device)},
    dtype=${JSON.stringify(dtype)},
)

# ---- Initial state: all qubits in |0> ------------------------------
dim = 2 ** n_sites
rho0 = np.zeros((dim, dim), dtype=complex)
rho0[0, 0] = 1.0

# ---- Run ------------------------------------------------------------
result = solver.run(
    rho0=rho0,
    t_span=(0.0, ${t_final}),
    dt=${dt},
    observables=Z_all,
    save_every=1,
)

# ---- Report ---------------------------------------------------------
print("Final trace:", result.norm[-1])
for s in range(n_sites):
    print(f"<Z_{'$'}{s}>(t_final) =", result.expect[s][-1].real)
`;

    out.textContent = code;
    if (window.Prism) {
      Prism.highlightElement(out);
    }
  }

  form.addEventListener('input',  emit);
  form.addEventListener('change', emit);

  if (copy) {
    copy.addEventListener('click', () => {
      navigator.clipboard.writeText(out.textContent || '').then(() => {
        copy.textContent = 'Copied!';
        setTimeout(() => { copy.innerHTML = '<i class="fa fa-copy"></i> Copy code'; }, 1500);
      });
    });
  }

  // Initial render
  emit();
})();
